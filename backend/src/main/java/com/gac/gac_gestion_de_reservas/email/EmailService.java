    private final JavaMailSender mailSender;

    @Override
    @Async
    public void sendConfirmationToken(String to, String email) {

        try {
            MimeMessage mimeMessage = mailSender.createMimeMessage();
            MimeMessageHelper helper =
                    new MimeMessageHelper(mimeMessage, "utf-8");
            helper.setText(email, true);
            helper.setTo(to);
            helper.setSubject("Confirme su email!");
            helper.setFrom("noreply.easytable@gmail.com");
            mailSender.send(mimeMessage);

        } catch (MessagingException e) {
            LOGGER.error("Fallo al enviar el email", e );
            throw new IllegalStateException("Fallo al enviar el email");
        }
    }

